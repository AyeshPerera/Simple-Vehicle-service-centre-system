    public void Show_data_Table()
    {
        ArrayList<EmployeeT> list=getUserList();
        DefaultTableModel model=(DefaultTableModel)Jemptble.getModel();
        model.setRowCount(0);
        Object[] row = new Object[8];
        for(int i=0;i<list.size();i++)
        {
            row[0]=list.get(i).getid();
            row[1]=list.get(i).getName();
            row[2]=list.get(i).getsect();
            row[3]=list.get(i).getAdr();
            row[4]=list.get(i).getTnmbr();
            row[5]=list.get(i).getDate();
            row[6]=list.get(i).getsal();
            row[7]=list.get(i).getGen();
            
            model.addRow(row);
        }
    }
    
    
