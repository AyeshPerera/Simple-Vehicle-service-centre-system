    public ArrayList<EmployeeT> getUserList(){
        
        ArrayList userlist=new ArrayList();
        
        Connection con=mn.getcon();
        
        String qyr="SELECT * FROM `employee`";
        Statement st;
        ResultSet rs;
        
        try{
            st=con.createStatement();
            rs=st.executeQuery(qyr);
            
            EmployeeT empt;
            
            while(rs.next()){
                
                empt=new EmployeeT(rs.getInt("E_Id"),rs.getString("E_Name"),rs.getString("Section"),rs.getString("Address"),rs.getString("T_Nmbr"),rs.getDate("Ass_Date"),rs.getInt("Ba_Sal"),rs.getString("Gender"),rs.getString("Password"));
                userlist.add(empt);
            }
                    
        }catch(Exception e){
            
            e.printStackTrace();
            
        }
        return userlist;
        
    }
    
